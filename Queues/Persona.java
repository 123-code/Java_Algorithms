package Queues;


public class Persona {
  private String Nombre;
  private int edad;

  public Persona(){
    
  }

public void setNombre(String _Nombre){
  this.Nombre = _Nombre;
}

public String getNombre(){
  return this.Nombre;
}

  public void setEdad(int _edad){
    this.edad = _edad;
  }

  public int getEdad(){
    return this.edad;
  }

    
}
